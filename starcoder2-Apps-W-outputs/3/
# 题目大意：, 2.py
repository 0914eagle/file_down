
# 题目大意：给你一个数字串，你需要在这个数字串中插入一个加号和一个等号，使得这个数字串满足等式的条件
# 思路：从左到右遍历，如果当前数字比前一个数字大，那么就在前一个数字后面插入一个加号，然后把当前数字插入到加号后面
# 注意：如果当前数字比前一个数字小，那么就在当前数字前面插入一个加号，然后把前一个数字插入到加号后面
# 注意：如果当前数字和前一个数字相等，那么就在当前数字前面插入一个加号，然后把前一个数字插入到加号后面

s = input()

ans = s[0]
for i in range(1, len(s)):
    if s[i] >= s[i-1]:
        ans += '+' + s[i]
    else:
        ans = ans + '+' + s[i-1] + '=' + s[i]
print(ans)
