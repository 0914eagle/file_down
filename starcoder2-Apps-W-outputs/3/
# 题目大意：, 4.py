
# 题目大意：给定一个数字串，在其中插入一个加号和一个等号，使得等号左边的数字加上等号右边的数字等于中间的数字
# 输入：数字串
# 输出：插入加号和等号后的数字串

# 思路：
# 1. 先找到等号左边的数字
# 2. 再找到等号右边的数字
# 3. 最后把加号和等号插入到数字串中

# 代码：
# 1. 先找到等号左边的数字
# 2. 再找到等号右边的数字
# 3. 最后把加号和等号插入到数字串中

s = input()

# 1. 先找到等号左边的数字
for i in range(len(s)):
    if int(s[:i]) + int(s[i:]) == int(s[i:i+len(s)//2]):
        print(s[:i] + '+' + s[i:i+len(s)//2] + '=' + s[i+len(s)//2:])
        break
